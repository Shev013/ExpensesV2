CREATE VIEW dbo.vExpenses
AS
SELECT        dbo.Assets.Description AS Asset, dbo.ExpensesTypes.Description AS Type, dbo.Expenses.Description, dbo.Expenses.ExpenseDate, dbo.Expenses.id, dbo.Expenses.RecordDate, dbo.Expenses.Value, 
                         dbo.Expenses.Observations, dbo.Expenses.codCurrency, dbo.ExpensesSubTypes.Description AS SubType, dbo.Assets.id AS AssetID
FROM            dbo.Expenses INNER JOIN
                         dbo.ExpensesSubTypes ON dbo.Expenses.SubType = dbo.ExpensesSubTypes.id INNER JOIN
                         dbo.ExpensesTypes ON dbo.ExpensesSubTypes.idType = dbo.ExpensesTypes.id LEFT OUTER JOIN
                         dbo.Assets ON dbo.Assets.id = dbo.Expenses.Asset

GO
